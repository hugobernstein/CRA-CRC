    ◊article{
      
      ◊p{A clinical research associate (CRA), also called a clinical monitor or trial monitor, is a health-care professional who performs many activities related to medical research, particularly clinical trials. Clinical research associates work in various settings, such as pharmaceutical companies, medical research institutes and government agencies.[1][2] Depending on the jurisdiction, different education and certification requirements may be necessary to practice as a clinical research associate.

      The main tasks of the CRA are defined by good clinical practice guidelines for monitoring clinical trials, such as those elaborated by the International Conference on Harmonisation of Technical Requirements for Registration of Pharmaceuticals for Human Use (ICH).}

      ◊sektion{Overview}
      ◊p{The main function of a clinical research associate is to monitor clinical trials. The CRA may work directly with the sponsor company of a clinical trial, as an independent freelancer or for a contract research organization (CRO). A clinical research associate ensures compliance with the clinical trial protocol, checks clinical site activities, makes on-site visits, reviews case report forms (CRFs), and communicates with clinical research coordinators.[3] Clinical research associates also "assure the protection of the rights, safety and well being of human study subjects."[4] Additionally, a CRA must "make certain that the scientific integrity of the data collected is protected and verified" and "assure that adverse events are correctly documented and reported."[4]

      A CRA is usually required to possess an academic degree in Life Sciences and needs to have a good knowledge of good clinical practice and local regulations.}
